package com.poi;

public class WorkInfo {
	private Double number;
	private String item;
	private String meting;
	private String bweek;
	private String tweek;
	private String problem;
	private String remark;
	
	public Double getNumber() {
		return number;
	}
	public void setNumber(Double number) {
		this.number = number;
	}
	public String getItem() {
		return item;
	}
	public void setItem(String item) {
		this.item = item;
	}
	public String getMeting() {
		return meting;
	}
	public void setMeting(String meting) {
		this.meting = meting;
	}
	public String getBweek() {
		return bweek;
	}
	public void setBweek(String bweek) {
		this.bweek = bweek;
	}
	public String getTweek() {
		return tweek;
	}
	public void setTweek(String tweek) {
		this.tweek = tweek;
	}
	public String getProblem() {
		return problem;
	}
	public void setProblem(String problem) {
		this.problem = problem;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
}
